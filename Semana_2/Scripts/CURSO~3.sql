--UNA VISTA QUE MUESTRE EL NOMBRE, DEPARTAMENTO Y PUESTO DE TODOS LOS TRABAJADORES
CREATE OR REPLACE VIEW V_TRABAJADOR_INFO AS
SELECT
    NOMBRE,
    DEPARTAMENTO,
    PUESTO
FROM
    TRABAJADOR;


SELECT * FROM V_TRABAJADOR_INFO;

