//MUESTRA EL RESUMEN DE VENTAS POR CLIENTE

CREATE OR REPLACE VIEW V_RESUMEN_VENTAS_CLIENTE AS
SELECT
    C.NOMBRE AS NOMBRE_CLIENTE,
    C.CIUDAD,
    SUM(V.CANTIDAD * P.PRECIO) AS TOTAL_GASTADO
FROM
    VENTA V
INNER JOIN CLIENTE C ON V.CLIENTE_ID = C.ID_CLIENTE
INNER JOIN PRODUCTO P ON V.PRODUCTO_ID = P.ID_PRODUCTO
GROUP BY
    C.NOMBRE, C.CIUDAD
ORDER BY
    TOTAL_GASTADO DESC;

SELECT * FROM V_RESUMEN_VENTAS_CLIENTE;

//CREA UN PROCEDIMIENTO ALMACENADO QUE MUESTRA LAS VENTAS REALIZADAS POR UN CLIENTE

CREATE OR REPLACE PROCEDURE MOSTRAR_VENTAS_CLIENTE (
    P_CLIENTE_ID IN NUMBER
)
AS
    CURSOR C_VENTAS IS
        SELECT
            V.ID_VENTA,
            P.NOMBRE AS PRODUCTO,
            V.CANTIDAD,
            P.PRECIO,
            V.CANTIDAD * P.PRECIO AS SUBTOTAL
        FROM
            VENTA V
        INNER JOIN PRODUCTO P ON V.PRODUCTO_ID = P.ID_PRODUCTO
        WHERE
            V.CLIENTE_ID = P_CLIENTE_ID;

    V_NOMBRE_CLIENTE CLIENTE.NOMBRE%TYPE;

BEGIN
    SELECT NOMBRE INTO V_NOMBRE_CLIENTE FROM CLIENTE WHERE ID_CLIENTE = P_CLIENTE_ID;

    DBMS_OUTPUT.PUT_LINE('VENTAS REALIZADAS POR: ' || V_NOMBRE_CLIENTE);
   

    FOR REC IN C_VENTAS LOOP
        DBMS_OUTPUT.PUT_LINE('VENTA ID: ' || REC.ID_VENTA || 
                             ', PRODUCTO: ' || REC.PRODUCTO || 
                             ', CANTIDAD: ' || REC.CANTIDAD || 
                             ', SUBTOTAL: ' || TO_CHAR(REC.SUBTOTAL, '9999.99'));
    END LOOP;

EXCEPTION
    WHEN NO_DATA_FOUND THEN
        DBMS_OUTPUT.PUT_LINE('EL CLIENTE CON ID ' || P_CLIENTE_ID || ' NO EXISTE O NO TIENE VENTAS.');
   
END;
/

EXECUTE MOSTRAR_VENTAS_CLIENTE(2);

//CREAR UN TRIGGER DE AUDITORÍA PARA REGISTRAR CUANDO SE REALICE UNA NUEVA VENTA

CREATE TABLE AUDITORIA_VENTAS (
    ID_AUDITORIA NUMBER PRIMARY KEY,
    ID_VENTA_AFECTADA NUMBER,
    FECHA_REGISTRO DATE,
    USUARIO_ACCION VARCHAR2(50),
    ACCION VARCHAR2(50)
);
CREATE SEQUENCE SEQ_AUDITORIA_VENTAS
START WITH 1
INCREMENT BY 1
NOCACHE;

CREATE OR REPLACE TRIGGER TRG_AUDITORIA_NUEVA_VENTA
AFTER INSERT ON VENTA
FOR EACH ROW
BEGIN
    INSERT INTO AUDITORIA_VENTAS (
        ID_AUDITORIA,
        ID_VENTA_AFECTADA,
        FECHA_REGISTRO,
        USUARIO_ACCION,
        ACCION
    )
    VALUES (
        SEQ_AUDITORIA_VENTAS.NEXTVAL,
        :NEW.ID_VENTA,
        SYSDATE,
        USER,
        'NUEVA_VENTA_REGISTRADA'
    );
END;
