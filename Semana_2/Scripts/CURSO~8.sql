CREATE OR REPLACE PROCEDURE DETALLE_CELULARES
IS
    CURSOR CELULARES_LISTA IS
        SELECT MARCA, MODELO, PRECIO FROM CELULAR;

    V_MARCA CELULAR.MARCA%TYPE;
    V_MODELO CELULAR.MODELO%TYPE;
    V_PRECIO CELULAR.PRECIO%TYPE;
BEGIN
    OPEN CELULARES_LISTA;
    LOOP
        FETCH CELULARES_LISTA INTO V_MARCA, V_MODELO, V_PRECIO; --RECUPERACION DE LA FILA
        EXIT WHEN CELULARES_LISTA%NOTFOUND; --SALIR CUANDO YA NO ENCUENTRE NINGUNA FILA
        DBMS_OUTPUT.PUT_LINE('EL CELULAR: ' || V_MARCA || ' ' || V_MODELO || ' TIENE UN PRECIO DE: ' || V_PRECIO);

END LOOP;
CLOSE CELULARES_LISTA;

END;
/
EXECUTE DETALLE_CELULARES;

