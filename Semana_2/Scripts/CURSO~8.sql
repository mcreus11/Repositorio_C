DECLARE CURSOR C_ESTUDIANTES_SISTEMAS IS
        SELECT ID, NOMBRE, CARRERA, PROMEDIO, GENERO
        FROM ALUMNO
        WHERE CARRERA = 'Sistemas';

    R_ESTUDIANTE C_ESTUDIANTES_SISTEMAS%ROWTYPE;

BEGIN
    DBMS_OUTPUT.PUT_LINE('ESTUDIANTES DE SISTEMAS');
    
    OPEN C_ESTUDIANTES_SISTEMAS;

    LOOP
        FETCH C_ESTUDIANTES_SISTEMAS INTO R_ESTUDIANTE;

        EXIT WHEN C_ESTUDIANTES_SISTEMAS%NOTFOUND;
        
        DBMS_OUTPUT.PUT_LINE('ID: ' || R_ESTUDIANTE.ID || 
                             ' | NOMBRE: ' || R_ESTUDIANTE.NOMBRE || 
                             ' | CARRERA: ' || R_ESTUDIANTE.CARRERA || 
                             ' | PROMEDIO: ' || R_ESTUDIANTE.PROMEDIO ||
                             ' | GÉNERO: ' || R_ESTUDIANTE.GENERO);
    END LOOP;

    CLOSE C_ESTUDIANTES_SISTEMAS;

END;
