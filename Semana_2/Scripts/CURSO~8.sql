DECLARE CURSOR C_TRABAJADOR IS
        SELECT NOMBRE, APELLIDO, EDAD
        FROM TRABAJADOR;

    V_NOMBRE TRABAJADOR.NOMBRE%TYPE;
    V_APELLIDO TRABAJADOR.APELLIDO%TYPE;
    V_EDAD TRABAJADOR.EDAD%TYPE;

BEGIN
    OPEN C_TRABAJADOR;

    LOOP
        FETCH C_TRABAJADOR INTO V_NOMBRE, V_APELLIDO, V_EDAD;

        EXIT WHEN C_TRABAJADOR%NOTFOUND;

        DBMS_OUTPUT.PUT_LINE('TRABAJADOR: ' || V_NOMBRE || ' ' || V_APELLIDO || ' | EDAD: ' || V_EDAD);
    END LOOP;

    CLOSE C_TRABAJADOR;

END;
