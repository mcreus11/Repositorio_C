-- MOSTRAR LAS TABLAS DE MULTIPLICAR DEL 1 AL 10
BEGIN
    FOR I IN 1..10 LOOP
        DBMS_OUTPUT.PUT_LINE('--- TABLA DEL ' || I || ' ---');
        FOR J IN 1..10 LOOP
            DBMS_OUTPUT.PUT_LINE(I || ' * ' || J || ' = ' || (I * J));
        END LOOP;
        DBMS_OUTPUT.PUT_LINE(' ');
    END LOOP;
END;
