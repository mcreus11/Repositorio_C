//Calcular la suma de los dígitos de un número
DECLARE
    V_NUMERO NUMBER := 12345678910;
    V_SUMA   NUMBER := 0;
    V_TEMP   NUMBER;
BEGIN
    V_TEMP := V_NUMERO;
    WHILE V_TEMP > 0 LOOP
        V_SUMA := V_SUMA + MOD(V_TEMP, 10);  
        V_TEMP := TRUNC(V_TEMP / 10);       
    END LOOP;

    DBMS_OUTPUT.PUT_LINE('LA SUMA DE LOS DÍGITOS DE ' || V_NUMERO || ' ES: ' || V_SUMA);
END;
