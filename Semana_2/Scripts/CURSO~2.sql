-- MOSTRAR LA CUENTA REGRESIVA DE 20 A 0 EN NÚMEROS PARES
BEGIN
    FOR I IN REVERSE 0..20 LOOP
        IF MOD(I, 2) = 0 THEN
            DBMS_OUTPUT.PUT_LINE(I);
        END IF;
    END LOOP;
END;
