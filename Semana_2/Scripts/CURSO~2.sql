-- MOSTRAR SI UN NÚMERO ES PRIMO
DECLARE
    V_NUMERO NUMBER := 17;
    V_CONTADOR NUMBER := 0;
BEGIN
    FOR I IN 1..V_NUMERO LOOP
        IF MOD(V_NUMERO, I) = 0 THEN
            V_CONTADOR := V_CONTADOR + 1;
        END IF;
    END LOOP;

    IF V_CONTADOR = 2 THEN
        DBMS_OUTPUT.PUT_LINE(V_NUMERO || ' ES PRIMO.');
    ELSE
        DBMS_OUTPUT.PUT_LINE(V_NUMERO || ' NO ES PRIMO.');
    END IF;
END;
/
