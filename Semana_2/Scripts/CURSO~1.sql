// PROCEDIMIENTO PARA BUSCAR UN TRABAJADOR POR SU NOMBRE
CREATE OR REPLACE PROCEDURE BUSCAR_TRABAJADOR_POR_NOMBRE (
    P_NOMBRE_BUSCADO IN TRABAJADOR.NOMBRE%TYPE
)
AS
    CURSOR C_TRABAJADORES IS
        SELECT ID, APELLIDO, PUESTO, SUELDO
        FROM TRABAJADOR
        WHERE NOMBRE = P_NOMBRE_BUSCADO;

    V_CONTADOR NUMBER := 0;
BEGIN
    DBMS_OUTPUT.PUT_LINE('RESULTADOS DE BUSQUEDA:  ' || P_NOMBRE_BUSCADO );

    FOR R_TRABAJADOR IN C_TRABAJADORES LOOP
        DBMS_OUTPUT.PUT_LINE('ID: ' || R_TRABAJADOR.ID || ' | APELLIDO: ' || R_TRABAJADOR.APELLIDO || 
                ' | PUESTO: ' || R_TRABAJADOR.PUESTO || 
                ' | SUELDO: ' || R_TRABAJADOR.SUELDO);
        V_CONTADOR := V_CONTADOR + 1;
    END LOOP;

    IF V_CONTADOR = 0 THEN
        DBMS_OUTPUT.PUT_LINE('NO SE ENCONTRO TRABAJADOR CON ESTE NOMBRE ');
    END IF;

END;
/


