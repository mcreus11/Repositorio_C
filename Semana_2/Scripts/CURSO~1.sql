// PROCEDIMIENTO PARA CONSULTAR EL SUELDO DE UN TRABAJADOR
CREATE OR REPLACE PROCEDURE CONSULTAR_SUELDO (
    P_ID_TRABAJADOR IN  TRABAJADOR.ID%TYPE,
    P_SUELDO        OUT TRABAJADOR.SUELDO%TYPE
)
AS
BEGIN
    SELECT SUELDO
    INTO P_SUELDO
    FROM TRABAJADOR
    WHERE ID = P_ID_TRABAJADOR;


END;
/


DECLARE
     V_SUELDO NUMBER(10, 2);
BEGIN
CONSULTAR_SUELDO(1, V_SUELDO);
    IF V_SUELDO IS NOT NULL THEN
        DBMS_OUTPUT.PUT_LINE('EL SUELDO DEL TRABAJADOR ES: ' || V_SUELDO);
    END IF;
END;
/

// PROCEDIMIENTO PARA AUMENTAR EL SUELDO DE LOS TRABAJADORES
CREATE OR REPLACE PROCEDURE AUMENTAR_SUELDO_GLOBAL (
    P_PORCENTAJE_AUMENTO IN NUMBER
)
AS
BEGIN
   
    UPDATE TRABAJADOR
    SET SUELDO = SUELDO * (1 + P_PORCENTAJE_AUMENTO / 100);

    DBMS_OUTPUT.PUT_LINE('SE HA APLICADO UN AUMENTO DEL ' || P_PORCENTAJE_AUMENTO || '% A TODOS LOS TRABAJADORES.');
    
END;
