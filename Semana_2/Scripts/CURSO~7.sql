END;

//RECIBE NOMBRE Y APELLIDO Y DEVUELVE LAS INICIALES

CREATE OR REPLACE FUNCTION OBTENER_INICIALES (
    P_NOMBRE IN NVARCHAR2,
    P_APELLIDO IN NVARCHAR2
)
RETURN NVARCHAR2
IS
    V_INICIALES NVARCHAR2(5);
BEGIN
    V_INICIALES := SUBSTR(P_NOMBRE, 1, 1) || '.' || SUBSTR(P_APELLIDO, 1, 1);
    RETURN (V_INICIALES);
END;
/

SELECT OBTENER_INICIALES('JOSUE ', 'HERNANDEZ') AS INICIALES FROM DUAL;

BEGIN
    DBMS_OUTPUT.PUT_LINE('LAS INICIALES SON SON: ' || OBTENER_INICIALES('JOSUE ', 'HERNANDEZ') );
END;

//DEVUELVE EL NOMBRE DEL MES CORRESPONDIENTE (1 AL 12)

CREATE OR REPLACE FUNCTION OBTENER_NOMBRE_MES (
    P_NUMERO_MES IN NUMBER
)
RETURN NVARCHAR2
IS
    V_FECHA_BASE DATE;
BEGIN
    IF P_NUMERO_MES < 1 OR P_NUMERO_MES > 12 THEN
        RETURN 'MES INVÁLIDO';
    END IF;
 
    V_FECHA_BASE := TO_DATE('01-' || P_NUMERO_MES || '-2000', 'DD-MM-YYYY');

    RETURN TO_CHAR(V_FECHA_BASE, 'MONTH');
END;
/

SELECT OBTENER_NOMBRE_MES(10) FROM DUAL;

BEGIN
    DBMS_OUTPUT.PUT_LINE('EL MES QUE INTRODUJO ES: ' || OBTENER_NOMBRE_MES(11) );
END;
