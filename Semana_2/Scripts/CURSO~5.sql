-- Tabla ESTUDIANTE
CREATE TABLE ESTUDIANTE (
    ID NUMBER PRIMARY KEY,
    NOMBRE VARCHAR2(50),
    APELLIDO VARCHAR2(50),
    CARRERA VARCHAR2(100)
);

-- Tabla MATERIA
CREATE TABLE MATERIA (
    ID NUMBER PRIMARY KEY,
    NOMBRE VARCHAR2(100),
    CREDITOS NUMBER
);

-- Tabla CALIFICACION
CREATE TABLE CALIFICACION (
    ID NUMBER PRIMARY KEY,
    ALUMNO_ID NUMBER,
    MATERIA_ID NUMBER,
    CALIFICACION NUMBER(3, 1),
    CONSTRAINT FK_ALUMNO FOREIGN KEY (ALUMNO_ID) REFERENCES ESTUDIANTE(ID),
    CONSTRAINT FK_MATERIA FOREIGN KEY (MATERIA_ID) REFERENCES MATERIA(ID)
);

