-- PROCEDIMIENTO PARA APLICAR UN DESCUENTO
CREATE OR REPLACE PROCEDURE APLICAR_DESCUENTO(P_DESCUENTO IN NUMBER) -- EL PORCENTAJE DESCUENTO 10%, 15%
IS
BEGIN
    UPDATE CELULAR
    SET PRECIO = PRECIO - (PRECIO * (P_DESCUENTO / 100));

    DBMS_OUTPUT.PUT_LINE('SE APLICO EL DESCUENTO DEL ' || P_DESCUENTO || '% A TODOS LOS CELULARES');
END;
/

