//PROCEDIMIENTO ALMACENADO COMO TAREA DE RESPALDO LO QUE EXISTA EN LA TABLA CELULAR 
CREATE OR REPLACE PROCEDURE PR_RESPALDO_CELULAR
IS
    --DECLARACION DE VARIABLES, CONSTANTES O CURSORES
BEGIN
    --INSTRUCCION DE ELIMINAR
    DELETE FROM CELULAR_RESP;
    --REALIZAR EL RESPALDO
    INSERT INTO CELULAR_RESP(ID_CELULAR, MARCA, MODELO, RAM, PROCESADOR, PRECIO)
        SELECT ID_CELULAR, MARCA, MODELO, RAM, PROCESADOR, PRECIO FROM CELULAR;
END;
/

