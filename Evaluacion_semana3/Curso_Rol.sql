-- 1. Crear la tabla CURSO_USUARIOS
CREATE TABLE CURSO_USUARIOS (
    ID_USUARIO      NUMBER PRIMARY KEY,
    NOMBRE          NVARCHAR2(100) NOT NULL,
    APP             NVARCHAR2(50) NOT NULL, 
    APM             NVARCHAR2(50),          
    SEXO            NVARCHAR2(25) NOT NULL,
    CORREO          NVARCHAR2(100) NOT NULL UNIQUE, 
    FECHA_NACIMIENTO DATE NOT NULL,
    FECHA_CREACION  DATE DEFAULT SYSDATE NOT NULL,
    ROL_ID          NUMBER NOT NULL,

    CONSTRAINT FK_ROL_ID
        FOREIGN KEY (ROL_ID)
        REFERENCES CURSO_ROLES(ID_ROL)
);

CREATE SEQUENCE CURSO_USUARIOS_SEQ START WITH 1 INCREMENT BY 1;

CREATE OR REPLACE TRIGGER CURSO_USUARIOS_TRG
BEFORE INSERT ON CURSO_USUARIOS
FOR EACH ROW
BEGIN
  :NEW.ID_USUARIO := CURSO_USUARIOS_SEQ.NEXTVAL;
END;
/

