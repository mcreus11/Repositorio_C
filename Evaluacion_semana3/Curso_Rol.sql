-- 1. Crear la tabla CURSO_ROLES
CREATE TABLE CURSO_ROLES (
    ID_ROL    NUMBER PRIMARY KEY,
    PRIVILEGIO NVARCHAR2(50) NOT NULL UNIQUE 
);

CREATE SEQUENCE CURSO_ROLES_SEQ START WITH 1 INCREMENT BY 1;

CREATE OR REPLACE TRIGGER CURSO_ROLES_TRG
BEFORE INSERT ON CURSO_ROLES
FOR EACH ROW
BEGIN
  :NEW.ID_ROL := CURSO_ROLES_SEQ.NEXTVAL;
END;
/

